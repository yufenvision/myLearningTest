public class BitOperationTest {
    public static void main(String[] args) {

        System.out.println(2 >> 1);//2转换为8位2进制数字00000010向右移1位00000001，得出十进制结果1
        System.out.println(2 << 1);//2转换为8位2进制数字00000010向左移1位00000100，登出十进制结果4


    }
}
